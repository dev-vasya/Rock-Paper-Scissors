function getComputerChoice() {
    let randomNumber = Math.floor((Math.random() * 3) + 1);
    let computerChoice;

    if (randomNumber == 1) {
        return computerChoice = "Rock"
    } else if (randomNumber == 2) {
        return computerChoice = "Paper"
    } else {
       return computerChoice = "Scissors"
    }
}




